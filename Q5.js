// Q5. Use the while loop to print number from 1 to 25 in ascending and descending order.

console.log("Number from 1 to 25 in ascending order");
let num1 = 1;
while (num1 <= 25) {
    console.log(num1);
    num1++;
}

console.log("Number from 1 to 25 in descending order");
let num2 = 25;
while (num2 >= 1) {
    console.log(num2);
    num2--;
}